    private void jB_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_salvarActionPerformed
        Consulta c = new Consulta();
        c.setData(jF_data.getText());
        c.setHoras(jF_horas.getText());
        
        Medico m = (Medico) jC_medico.getSelectedItem();
        JOptionPane.showMessageDialog(null, m.getId_medico()+" "+m.getNome());
        c.setMedico(m);
        
        Paciente p = (Paciente) jC_paciente.getSelectedItem();
        JOptionPane.showMessageDialog(null, p.getId_pacinte()+" "+p.getNome());
        c.setPaciente(p);
        
        c.setFuncionario(funcionario);
        
        ConsultaDAO cdao = new ConsultaDAO();
        cdao.cadastrar(c);
    }//GEN-LAST:event_jB_salvarActionPerformed

